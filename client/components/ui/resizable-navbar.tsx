type NavbarButtonProps<T extends React.ElementType = "a"> = {
  href?: string;
  as?: T;
  children: React.ReactNode;
  className?: string;
  variant?:
    | "default"
    | "destructive"
    | "outline"
    | "secondary"
    | "ghost"
    | "link";
} & Omit<React.ComponentPropsWithoutRef<T>, "as" | "children" | "className">;

export const NavbarButton = <T extends React.ElementType = "a">({
  href,
  as,
  children,
  className,
  variant = "default",
  ...props
}: NavbarButtonProps<T>) => {
  const Tag = (as || "a") as React.ElementType;

  const baseStyles =
    "px-4 py-2 rounded-md button text-black text-sm font-medium relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-block text-center";

  const variantStyles = {
    default: "bg-primary text-primary-foreground hover:bg-primary/90 h-9 px-4 py-2 has-[>svg]:px-3",
    destructive:
      "bg-destructive text-white hover:bg-destructive/90 focus-visible:ring-destructive/20 dark:focus-visible:ring-destructive/40 dark:bg-destructive/60",
    outline:
      "border bg-background shadow-xs hover:bg-accent hover:text-accent-foreground dark:bg-input/30 dark:border-input dark:hover:bg-input/50",
    secondary: "bg-secondary text-secondary-foreground hover:bg-secondary/80",
    ghost:
      "hover:bg-accent hover:text-accent-foreground dark:hover:bg-accent/50",
    link: "text-primary underline-offset-4 hover:underline",
  };

  const componentProps = {
    href,
    className: cn(baseStyles, variantStyles[variant], className),
    ...props,
  };

  return React.createElement(Tag, componentProps as any, children);
};
